/// Metadata for a single IBAN-supporting country.
class IbanCountryInfo {
  /// Two-letter ISO 3166-1 alpha-2 country code (e.g. `'DE'`).
  final String countryCode;

  /// Human-readable country name.
  final String countryName;

  /// Fixed IBAN length for this country (including the 4-char prefix).
  final int ibanLength;

  /// Whether the country is part of the SEPA payment area.
  final bool isSepa;

  /// Whether this entry is from the experimental / partial-IBAN list.
  ///
  /// Experimental countries (mostly in Africa) have started adopting the
  /// IBAN format but are not yet in the official ISO 13616 registry.
  final bool isExperimental;

  /// A well-known sample IBAN for documentation and testing purposes.
  final String example;

  const IbanCountryInfo({
    required this.countryCode,
    required this.countryName,
    required this.ibanLength,
    required this.isSepa,
    required this.example,
    this.isExperimental = false,
  });

  @override
  String toString() =>
      'IbanCountryInfo($countryCode, length: $ibanLength, sepa: $isSepa)';
}

